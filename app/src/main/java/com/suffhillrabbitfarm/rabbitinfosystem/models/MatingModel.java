package com.suffhillrabbitfarm.rabbitinfosystem.models;

public class MatingModel {
    String date;
    String partner;
    String newBorns;
    String deaths;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPartner() {
        return partner;
    }

    public void setPartner(String partner) {
        this.partner = partner;
    }

    public String getNewBorns() {
        return newBorns;
    }

    public void setNewBorns(String newBorns) {
        this.newBorns = newBorns;
    }

    public String getDeaths() {
        return deaths;
    }

    public void setDeaths(String deaths) {
        this.deaths = deaths;
    }
}
